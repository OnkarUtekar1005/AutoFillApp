"""
Full AOC-4 field schema — models the complete MCA AOC-4 form (~201 items),
per AOC4_FORM_FIELDS.md (derived from a real filed form).

Each field carries a `source`:
  AUTO   — extractable from the client's financial documents (Claude/regex target)
  MANUAL — the CA/CS must type it (SRNs, product codes, service-provider info, …)
  MCA    — generated by the MCA portal itself (SRN, filing date) — never filled here

Previous-year (PY) variants for balance-sheet and P&L line items are generated
automatically (see PY generation near the bottom) as `<key>_prior_year`, matching
the Current/Previous columns on the form and the mapper's prior-year handling.
"""
from dataclasses import dataclass, field


