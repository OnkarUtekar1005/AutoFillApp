    key: str
    label: str
    section: str
    data_type: str          # numeric | string | date | boolean | enum | text
    mandatory: bool
    synonyms: list[str] = field(default_factory=list)
    enum_values: list[str] | None = None
    source: str = "AUTO"     # AUTO | MANUAL | MCA
    has_prior_year: bool = False


