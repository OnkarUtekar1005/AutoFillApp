# ── Auto-generate previous-year variants for BS/P&L line items ───────────────
def _prior_year_variants(base: list[FieldDef]) -> list[FieldDef]:
    out: list[FieldDef] = []
    for f in base:
        if not f.has_prior_year:
            continue
        out.append(FieldDef(
            key=f"{f.key}_prior_year",
            label=f"{f.label} (Previous Year)",
            section="prior_year",
            data_type=f.data_type,
            mandatory=False,
            synonyms=[f"previous year {f.label}", f"prior year {f.label}"],
            enum_values=f.enum_values,
            source=f.source,
            has_prior_year=False,
        ))
    return out


